package cn.tuyucheng.taketoday.dagger.intro;

/**
 * Engine of a {@link Car}.
 */
public class Engine {

	/**
	 * Starts the engine.
	 */
	public void start() {
		System.out.println("Engine started");
	}

	/**
	 * Stops the engine.
	 */
	public void stop() {
		System.out.println("Engine stopped");
	}
}
